ordinal_numbers = list(range(1, 10))
print(ordinal_numbers)

#Printing ordinal numbers with if statement
for ordinal_numbers in ordinal_numbers:
	if ordinal_numbers == 1:
		print('\n1st')
	elif ordinal_numbers == 2:
		print('\n2st')
	elif ordinal_numbers == 3:
		print('\n3rd')
	elif ordinal_numbers == 4:
		print('\n4th')
	elif ordinal_numbers == 5:
		print('\5th')
	elif ordinal_numbers == 6:
		print('\6th')
	elif ordinal_numbers == 7:
		print('\n7th')
	elif ordinal_numbers == 8:
		print('\n8th')
	else: 
		print('\n9th')